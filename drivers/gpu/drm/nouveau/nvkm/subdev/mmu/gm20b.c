static int
gm20b_mmu_ctor(struct nvkm_object *parent, struct nvkm_object *engine,
	       struct nvkm_oclass *oclass, void *data, u32 size,
	       struct nvkm_object **pobject)
{
	struct gk20a_mmu_priv *priv;
	int ret;

	ret = gk20a_mmu_ctor(parent, engine, oclass, data, size, pobject);
	if (ret)
		return ret;

	priv = (void *)*pobject;
	priv->base.pgt_bits  = 26 - 12;
	priv->base.lpg_shift = 16;
	priv->base.storage_type_map = gm20b_pte_storage_type_map;

	return 0;
}

struct nvkm_oclass
gm20b_mmu_oclass = {
	.handle = NV_SUBDEV(MMU, 0x2b),
	.ofuncs = &(struct nvkm_ofuncs) {
		.ctor = gm20b_mmu_ctor,
		.dtor = _nvkm_mmu_dtor,
		.init = _nvkm_mmu_init,
		.fini = _nvkm_mmu_fini,
	},
};
